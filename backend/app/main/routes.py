@bp.route('/posts/<int:post_id>/downvotes', methods=['POST'])
@login_required
def downvotes(post_id):
    post = Post.query.filter_by(id=post_id).first_or_404()
    if not post.downvotes:
        post.downvotes = 0
    post.downvotes += 1
    db.session.commit()
    return {"downvotes": post.downvotes, "id": post.id}


